  double _confidenceFor({
    required String intent,
    required AIRecommendation? response,
    required String input,
    required double intentScore,
    required double latentConfidence,
  }) {
    final double? responseConfidence = response?.confidence;
    if (responseConfidence != null) {
      return responseConfidence.clamp(0.0, 1.0);
    }
    if (input.trim().isEmpty) return 0.35;
    if (intent == 'general_query') {
      return ((intentScore + latentConfidence) / 2).clamp(0.0, 1.0);
    }
    return ((intentScore * 0.6) + (latentConfidence * 0.4)).clamp(0.0, 1.0);
  }

