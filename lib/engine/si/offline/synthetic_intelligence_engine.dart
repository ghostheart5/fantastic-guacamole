  SILatentInputs _inferLatent(String text, SINonTextInputs nonText) {
    final String lowered = text.toLowerCase();
    final List<String> behavior = nonText.behaviorPatterns
        .map((String e) => e.toLowerCase())
        .toList();
    final bool hasPausePattern = behavior.any(
      (String p) => p.contains('pause') || p.contains('hesitat'),
    );

    double frustration = 0.1;
    double excitement = 0.1;
    double confusion = 0.1;
    double confidence = 0.5;
    double hesitation = hasPausePattern ? 0.55 : 0.2;

    if (lowered.contains('stuck') ||
        lowered.contains('annoyed') ||
        lowered.contains('frustrat')) {
      frustration = 0.75;
      confidence = 0.35;
    }
    if (lowered.contains('great') ||
        lowered.contains('awesome') ||
        lowered.contains('excited')) {
      excitement = 0.8;
      confidence = 0.75;
    }
    if (lowered.contains('confused') ||
        lowered.contains('not sure') ||
        lowered.contains('unclear')) {
      confusion = 0.72;
      confidence = 0.3;
      hesitation = 0.6;
    }

    if ((nonText.voiceToText ?? '').isNotEmpty && lowered.isEmpty) {
      confidence = 0.6;
    }

    return SILatentInputs(
      frustration: frustration,
      excitement: excitement,
      confusion: confusion,
      confidence: confidence,
      hesitation: hesitation,
    );
  }

  List<String> _extractKeywords(String text, List<String> imageLabels) {
    final Set<String> stop = <String>{
      'the',
      'and',
      'for',
      'with',
      'this',
      'that',
      'what',
      'should',
      'would',
      'could',
      'you',
      'your',
      'from',
      'have',
      'about',
    };
    final List<String> words = text
        .replaceAll(RegExp(r'[^a-z0-9\s]'), ' ')
        .split(RegExp(r'\s+'))
        .where((String w) => w.length > 2 && !stop.contains(w))
        .toList();
    return <String>{...words.take(8), ...imageLabels.take(4)}.toList();
  }

