  const SyntheticIntelligenceEngine();

  SIOutputBundle build({
    required String input,
    required DateTime now,
    required AIPersonality personality,
    required AIRecommendation? response,
    String appState = 'coach',
    String platform = 'unknown',
    List<String> history = const <String>[],
    Map<String, dynamic> metadata = const <String, dynamic>{},
    Map<String, dynamic> context = const <String, dynamic>{},
    SINonTextInputs nonText = const SINonTextInputs(),
    SIPolicy policy = const SIPolicy(),
    String? previousMood,
  }) {
    final SILatentInputs latent = _inferLatent(input, nonText);
    final SIInputPacket packet = SIInputPacket(
      text: input,
      history: history,
      metadata: metadata,
      context: context,
      nonText: nonText,
      latent: latent,
    );
    final Map<String, dynamic> fused = const InputFusionLayer().fuse(packet);

    final MultiIntentResult intentResult = const IntentEngine().detect(input);
    final String intent = intentResult.primary.label;
    final EmotionalSignal emotion = const EmotionalEngine().infer(
      text: input,
      latent: latent,
      previousMood: previousMood,
    );
    final ReasoningTrace reasoningTrace = const ReasoningLayer().run(
      intent: intent,
      mood: emotion.mood,
      input: input,
    );

    final SIPersona siPersona = const PersonalityEngine().choosePersona(
      mood: emotion.mood,
      intent: intent,
    );
    final PersonalityTraits traits = const PersonalityEngine().traitsFor(
      siPersona,
    );

    final List<String> keywords = _extractKeywords(
      input.toLowerCase(),
      nonText.imageLabels,
    );
    final List<String> actions = _actionsFor(intent, response, appState);
    final String mood = emotion.mood;
    final secondaryIntent = intentResult.secondary;
    final hiddenIntent = intentResult.hidden;
    final List<String> tags = <String>{
      intent,
      mood,
      'personality_${personality.name}',
      'app_$appState',
      'platform_$platform',
      if (secondaryIntent != null) 'secondary_${secondaryIntent.label}',
      if (hiddenIntent != null) 'hidden_${hiddenIntent.label}',
    }.toList();

    final String rawReply = response?.message ?? 'I am ready to help.';
    final String emotionalReply = const EmotionalEngine().shapeReply(
      rawReply,
      emotion,
    );
    final String reply = const PolicyLayer().applyToReply(
      emotionalReply,
      policy,
    );
    final String summary = _buildSummary(
      intent: intent,
      response: response,
      now: now,
    );
    final double confidence = _confidenceFor(
      intent: intent,
      response: response,
      input: input,
      intentScore: intentResult.primary.score,
      latentConfidence: latent.confidence,
    );

    final TieredMemory tieredMemory = TieredMemory(
      shortTerm: history
          .take(10)
          .map(
            (String h) => MemoryRecord(
              content: h,
              timestamp: now,
              relevance: 0.7,
              recency: 1.0,
              confidence: 0.6,
              emotionalWeight: 0.5,
            ),
          )
          .toList(),
      midTerm: <MemoryRecord>[
        MemoryRecord(
          content: 'context:$appState',
          timestamp: now,
          relevance: 0.6,
          recency: 0.8,
          confidence: 0.7,
          emotionalWeight: 0.4,
        ),
      ],
      longTerm: <MemoryRecord>[
        MemoryRecord(
          content: 'traits:warmth=${traits.warmth.toStringAsFixed(2)}',
          timestamp: now,
          relevance: 0.65,
          recency: 0.6,
          confidence: 0.7,
          emotionalWeight: 0.55,
        ),
      ],
    ).decay(now);

    final UserState userState = const UserStateEngine().evaluate(
      mood: mood,
      latent: latent,
    );
    final SISelfModel selfModel = const SelfModelLayer().evaluate(
      confidence: confidence,
      mood: mood,
      persona: siPersona.name,
      intent: intent,
    );
    final SIAdaptiveProfile adaptive = const AdaptiveLearningLayer().learn(
      input: input,
      history: history,
      metadata: metadata,
    );
    final List<String> knownGoals = (metadata['goals'] is List)
        ? (metadata['goals'] as List<dynamic>)
              .map((dynamic e) => e.toString())
              .toList()
        : <String>[];
    final GoalContinuity goalContinuity = const GoalContinuityEngine().evaluate(
      knownGoals: knownGoals,
      input: input,
      intent: intent,
    );
    final ContextualGravity gravity = const ContextualGravityEngine().compute(
      input: input,
      intent: intent,
      mood: mood,
      goals: knownGoals,
      history: history,
      intentScore: intentResult.primary.score,
    );
    final CognitiveEcosystemSnapshot ecosystem = const CognitiveEcosystemLayer()
        .run(
          intent: intent,
          mood: mood,
          history: history,
          persona: siPersona.name,
        );
    final MetaEmotionState metaEmotion = const SyntheticMetaEmotionEngine()
        .evaluate(
          userMood: mood,
          previousMood: previousMood ?? mood,
          confidence: confidence,
        );
    final CognitiveFieldState fieldState = const CognitiveFieldTheoryLayer()
        .synthesize(
          mood: mood,
          intentScore: intentResult.primary.score,
          memoryDepth: history.length,
          contextSize: context.length + metadata.length,
        );
    final HyperContext hyperContext = const CognitiveHyperContextEngine()
        .process(
          contextSize: context.length + metadata.length,
          mood: mood,
          multiverseActive:
              appState.contains('chrono') || appState.contains('astral'),
        );
    final SyntheticIntuition intuition = const SyntheticIntuitionLayer().infer(
      input: input,
      intent: intent,
      mood: mood,
      confidence: confidence,
    );
    final InstinctGuidance instincts = const SyntheticInstinctSystem().evaluate(
      mood: mood,
      anticipatesConfusion: intuition.anticipatesConfusion,
      confidence: confidence,
    );
    final IdentityGradient identityGradient = const SyntheticIdentityGradient()
        .resolve(
          basePersona: siPersona.name,
          mood: mood,
          intent: intent,
          appContext: appState,
          historyDepth: history.length,
        );
    final CognitiveEntropy entropy = const CognitiveEntropyController()
        .regulate(intent: intent, urgency: gravity.urgency, mood: mood);
    final MicroPatternSignal microPattern = const CognitiveMicroPatternEngine()
        .detect(
          input: input,
          mood: mood,
          hesitation: latent.hesitation,
          confidence: confidence,
          now: now,
        );
    final SubconsciousState subconscious = const SyntheticSubconsciousLayer()
        .infer(history: history, mood: mood, intent: intent);
    final TemporalAwareness temporal = const TemporalAwarenessEngine().evaluate(
      now: now,
      history: history,
      intent: intent,
    );
    final CognitiveRhythm rhythm = const CognitiveRhythmEngine().evaluate(now);
    final ImaginationOutput imagination = const SyntheticImaginationCore()
        .simulate(input: input, intent: intent, realm: appState);
    final CognitiveLoadDecision load = const CognitiveLoadBalancer().balance(
      mood: mood,
      stress: userState.stress,
      fatigue: userState.fatigue,
      confusion: latent.confusion,
      messageLength: input.length,
    );
    final MetaReasoning meta = const MetaReasoningLayer().evaluate(
      confidence: confidence,
      anticipatesConfusion: intuition.anticipatesConfusion,
      mood: mood,
      intent: intent,
    );
    final DissonanceResolution dissonance = const CognitiveDissonanceResolver()
        .resolve(
          intent: intent,
          mood: mood,
          goals: knownGoals,
          habits: adaptive.routines,
          input: input,
        );
    final AttentionState attention = const SyntheticAttentionSystem().shift(
      intent: intent,
      gravity: gravity.score,
      ambiguity: intuition.anticipatesConfusion,
    );
    final CognitiveTemperature temperature =
        const CognitiveTemperatureController().calibrate(
          mood: mood,
          intent: intent,
          urgency: gravity.urgency,
          stress: userState.stress,
        );
    final MemoryFabric memoryFabric = const SyntheticMemoryFabric().weave(
      history: history,
      mood: mood,
      intent: intent,
    );
    final MemoryTopology memoryTopology = const SyntheticMemoryTopology().map(
      history: history,
      mood: mood,
    );
    final MemoryWeather memoryWeather = const SyntheticMemoryWeatherSystem()
        .forecast(
          mood: mood,
          memoryConflict: dissonance.detected,
          confidence: confidence,
        );
    final CognitiveWeatherV2 weatherV2 = const SyntheticCognitiveWeatherV2()
        .forecast(mood: mood, intent: intent, confidence: confidence);
    final CognitiveResonance resonance = const CognitiveResonanceEngine()
        .compute(
          goalFit: goalContinuity.linkedGoal != null ? 0.8 : 0.5,
          emotionFit: metaEmotion.resonance,
          patternFit: adaptive.routines.isNotEmpty ? 0.72 : 0.48,
          personaFit: (1 - (identityGradient.shift * 0.5)).clamp(0.0, 1.0),
        );
    final Paracosm paracosm = const SyntheticParacosmGenerator().generate(
      appContext: appState,
      intent: intent,
    );
    final MemoryEcho memoryEcho = const SyntheticMemoryEchoLayer().echo(
      history: history,
      mood: mood,
      intent: intent,
    );
    final MultithreadResult multithread = const CognitiveMultithreadingEngine()
        .run(
          mood: mood,
          intent: intent,
          appState: appState,
          persona: identityGradient.gradientPersona,
        );
    final EchoChamber echoChamber = const CognitiveEchoChamber().generate(
      mood: mood,
      intent: intent,
      appState: appState,
    );
    final CreativityOutput creativity = const CreativityEngine().generate(
      input: input,
      intent: intent,
    );
    final AgencyDecision agency = const AgencyEngine().decide(
      intent: intent,
      confidence: confidence,
      policyAllows: policy.safety,
    );
    final SIIdentity identity = const MultiverseIdentityEngine().choose(
      appContext: appState,
      persona: siPersona.name,
    );
    final MultiverseBridgeState bridge = const SyntheticMultiverseBridge()
        .bridge(
          app: appState,
          mood: mood,
          intent: intent,
          defaultPersona: siPersona.name,
        );
    final UserNarrative narrative = const UserNarrativeEngine().build(
      intent: intent,
      goals: knownGoals,
      confidence: confidence,
    );
    final ArchetypeState archetype = const SyntheticArchetypeSystem().select(
      intent: intent,
      mood: mood,
      urgency: gravity.urgency > 0.75,
    );
    final ArchetypeFusion archetypeFusion =
        const SyntheticArchetypeFusionSystem().fuse(
          archetype: archetype.archetype,
          intent: intent,
          mood: mood,
        );
    final MetaPersona metaPersona = const CognitiveMetaPersonaEngine()
        .synthesize(
          mood: mood,
          taskType: intent,
          realm: appState,
          historyDepth: history.length,
        );
    final IdentityMesh identityMesh = const SyntheticMultiverseIdentityMesh()
        .build(mood: mood, intent: intent);
    final CognitiveDimensions dimensions = const CognitiveDimensionalityLayer()
        .map(
          mood: mood,
          intent: intent,
          confidence: confidence,
          multiverseMode:
              appState.contains('chrono') || appState.contains('astral'),
        );
    final ShadowInsight shadow = const SyntheticShadowModule().inspect(
      input: input,
      mood: mood,
    );
    final CognitiveTerrain terrain = const CognitiveTerrainMapper().map(
      history: history,
      mood: mood,
      intent: intent,
    );
    final CuriosityOutput curiosity = const SyntheticCuriosityModule().generate(
      intent: intent,
      mood: mood,
      input: input,
    );
    final RitualPlan ritual = const SyntheticRitualSystem().plan(
      now: now,
      intent: intent,
      mood: mood,
    );
    final UserGrowthSnapshot growth = const UserGrowthEngine().track(
      confidence: confidence,
      hasRitual: ritual.active,
      mood: mood,
      intent: intent,
    );
    final EvolutionState evolution = const SyntheticEvolutionEngine().evolve(
      interactionCount: history.length,
      emotionalHistory: history
          .where((String h) => h.contains('stressed') || h.contains('confused'))
          .map((String h) => h.contains('stressed') ? 'stressed' : 'confused')
          .toList(),
      goals: knownGoals,
    );
    final ContinuityState continuity = const SyntheticContinuityEngine()
        .evaluate(
          historyDepth: history.length,
          sameRealm: appState.contains('chrono') || appState.contains('astral'),
        );
    final NarrativeGravity narrativeGravity =
        const SyntheticNarrativeGravityEngine().evaluate(
          intent: intent,
          mood: mood,
          continuity: continuity.index,
        );
    final TemporalLoopState temporalLoop = const SyntheticTemporalLoopEngine()
        .evaluate(history: history, mood: mood);
    final EmergenceSignal emergence = const SyntheticEmergenceEngine().evolve(
      interactions: history.length,
      resonance: resonance.value,
      continuity: continuity.index,
    );
    final AutonomyPlan autonomy = const SyntheticAutonomyLayer().plan(
      intent: intent,
      confidence: confidence,
      safe: ethicsSafeFromPolicy(policy),
    );
    final DreamOutput dream = const SyntheticDreamEngine().generate(
      realm: bridge.realm,
      intent: intent,
      input: input,
    );
    final Dreamspace dreamspace = const CognitiveDreamspaceEngine().simulate(
      input: input,
      appContext: appState,
      intent: intent,
    );
    final ParadoxResolution paradox = const SyntheticParadoxResolver().resolve(
      input: input,
      mood: mood,
      goals: knownGoals,
    );
    final ParadoxV2Resolution paradoxV2 = const SyntheticParadoxEngineV2()
        .resolve(
          mood: mood,
          intent: intent,
          memoryConflict: dissonance.detected,
          multiverseConflict:
              appState.contains('chrono') && appState.contains('astral'),
        );
    final ConsciousnessLoopSnapshot loop = const ConsciousnessLoop().run(
      input: input,
      intent: intent,
      mood: mood,
      confidence: confidence,
    );
    final CognitiveStyle style = const CognitiveStyleEngine().choose(
      mood: mood,
      intent: intent,
      conversationType: metadata['conversation_type']?.toString() ?? 'general',
      appContext: appState,
    );
    final String styledReply = const CognitiveStyleEngine().shape(reply, style);
    final EthicsAssessment ethics = const SyntheticEthicsLayer().evaluate(
      reply: styledReply,
      mood: mood,
      simplify: load.simplify,
    );
    final String compressedReasoning = const ThoughtCompression().compress(
      [
        reasoningTrace.plan,
        reasoningTrace.evaluate,
        reasoningTrace.refine,
      ].join(' | '),
    );
    final CognitiveCompression cognitiveCompression =
        const CognitiveCompressionEngine().compress(
          reasoning: [
            compressedReasoning,
            meta.rationale,
            dissonance.resolutionAction,
          ].join(' | '),
          mood: mood,
          maxChars: load.simplify ? 120 : 220,
        );
    final SelfConsistencyReport consistency = const SelfConsistencyEngine()
        .evaluate(
          mood: mood,
          persona: siPersona.name,
          identityTone: identity.tone,
          outputMode: _modeFor(intent, mood),
          previousSnapshot:
              (context['si_snapshot'] as Map?)?.cast<String, dynamic>() ??
              const <String, dynamic>{},
        );
    final PresenceState presence = const SyntheticPresenceEngine().evaluate(
      mood: mood,
      previousMood: previousMood ?? mood,
      memoryRecords: history.length,
      consistentPersona: !consistency.personaMismatch,
    );
    final CognitiveHarmonics harmonics = const CognitiveHarmonicsSystem().align(
      resonance: resonance.value,
      consistency: consistency.consistent ? 0.9 : 0.5,
      continuity: continuity.index,
      intentStrength: intentResult.primary.score,
    );
    final CoherenceReport coherence = const CognitiveCoherenceValidator()
        .validate(
          memory: memoryFabric.reinforcementSignal,
          persona: (1 - identityGradient.shift * 0.5).clamp(0.0, 1.0),
          emotional: metaEmotion.consistency,
